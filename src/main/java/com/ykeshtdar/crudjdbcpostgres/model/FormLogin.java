package com.ykeshtdar.crudjdbcpostgres.model;

import lombok.*;



public class FormLogin {
    Integer id;
    String email;
    String password;

    public FormLogin() {
    }

    public FormLogin(Integer id, String email, String password) {
        this.id = id;
        this.email = email;
        this.password = password;
    }

    public Integer getId() {
        return id;
    }

    public String getEmail() {
        return email;
    }

    public String getPassword() {
        return password;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
